package todo_list;

/**
 * 게시글 데이터 객체
 * @author Yn
 *
 */
public class ToDoVO {

	private int number;
	private boolean don;
	private String todo;
	
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public boolean isDon() {
		return don;
	}
	public void setDon(boolean don) {
		this.don = don;
	}
	public String getTodo() {
		return todo;
	}
	public void setTodo(String todo) {
		this.todo = todo;
	}
	
	
	


	
	
}
